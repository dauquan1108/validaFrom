'use strict';

const isValidateInputEmail = (valueEmail) => {
	/* reg exp để kiểm tra xem chuỗi định dạng email */
	// eslint-disable-next-line no-useless-escape
	const regexp = /^(([^<>()\[\]\\.,;:~!%^$#=?/`\s@"]+(\.[^<>()\[\]\\.,;:~!%^$#=?/\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
	return regexp.test(valueEmail);
};

const removeAscent = (str) => {
	if (str === null || str === undefined) return str;
	str = str.toLowerCase();
	str = str.replace(/à|á|ạ|ả|ã|â|ầ|ấ|ậ|ẩ|ẫ|ă|ằ|ắ|ặ|ẳ|ẵ/g, "a");
	str = str.replace(/è|é|ẹ|ẻ|ẽ|ê|ề|ế|ệ|ể|ễ/g, "e");
	str = str.replace(/ì|í|ị|ỉ|ĩ/g, "i");
	str = str.replace(/ò|ó|ọ|ỏ|õ|ô|ồ|ố|ộ|ổ|ỗ|ơ|ờ|ớ|ợ|ở|ỡ/g, "o");
	str = str.replace(/ù|ú|ụ|ủ|ũ|ư|ừ|ứ|ự|ử|ữ/g, "u");
	str = str.replace(/ỳ|ý|ỵ|ỷ|ỹ/g, "y");
	str = str.replace(/đ/g, "d");
	return str;
};

const isValidaInputName = (valueName) => {
	const regexName = /^[a-zA-Z ]{2,}$/g;
	return regexName.test(removeAscent(valueName));
};

const isValidaPassword = (valuePassword) => {
	const passwords = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/;
	return passwords.test(valuePassword);
};

export { isValidaInputName, isValidateInputEmail, isValidaPassword };
